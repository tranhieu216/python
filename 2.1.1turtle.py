import turtle         # познакомили с пакетом turtle (черепаха)


t = turtle.Turtle()   # сделали черепаху, назвали черепаху t
t.shape("turtle")
t.width(3)            # ширина кисти


t.forward(75)         # вперед 75
t.left(90)            # налево на 90 градусов
t.forward(75)         # вперед 75

t.right(90)

t.forward(75)         # вперед 75
t.left(90)            # налево на 90 градусов
t.forward(75)         # вперед 75

turtle.done()